export default function EditFlag() {
  const { user } = useAuth();
  const { isDark } = useTheme();
  const colors = getThemeColors(isDark);
  const { showToast } = useAppToast();
  const { t } = useTranslation('profile');
  const { t: tErrors } = useTranslation('errors');
  const [loading, setLoading] = useState(false);
  const [initialLoading, setInitialLoading] = useState(true);
  const [searchQuery, setSearchQuery] = useState('');
  const [selectedCountryCode, setSelectedCountryCode] = useState<string | null>(null);
  const [currentCountryCode, setCurrentCountryCode] = useState<string | null>(null);

  useEffect(() => {
    loadCurrentFlag();
  // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [user]);

  const loadCurrentFlag = async () => {
    if (!user) return;
    
    try {
      const { data } = await getPlayerProfile(user.id);
      if (data?.nationality_code) {
        setCurrentCountryCode(data.nationality_code);
        setSelectedCountryCode(data.nationality_code);
      }
    } catch (error) {
      showToast(t('editFlag.loadError'), { type: 'error' });
    } finally {
      setInitialLoading(false);
    }
  };

  const handleCountrySelect = (countryCode: string) => {
    setSelectedCountryCode(countryCode);
  };

  const handleSave = async () => {
    if (!user) return;

    if (!selectedCountryCode) {
      showToast(t('editFlag.selectFlag'), { type: 'error' });
      return;
    }

    setLoading(true);
    try {
      const { error } = await createOrUpdatePlayerProfile(user.id, {
        nationality_code: selectedCountryCode,
        nationality_name: COUNTRIES.find(c => c.code === selectedCountryCode)?.name || null,
      });

      if (error) {
        showToast(t('editFlag.saveError'), { type: 'error' });
      } else {
        showToast(t('editFlag.saveSuccess'), { type: 'success' });
        router.back();
      }
    } catch (error) {
      showToast(tErrors('generic.tryAgain'), { type: 'error' });
    } finally {
      setLoading(false);
    }
  };

  const handleCancel = () => {
    router.back();
  };

  // Filter countries based on search query
  const filteredCountries = COUNTRIES.filter(country =>
    country.name.toLowerCase().includes(searchQuery.toLowerCase()) ||
    country.code.toLowerCase().includes(searchQuery.toLowerCase())
  );

  const hasChanges = selectedCountryCode !== currentCountryCode;

  const renderCountryItem = ({ item }: { item: Country }) => (
    <TouchableOpacity
      onPress={() => handleCountrySelect(item.code)}
      activeOpacity={0.7}
      style={{
        flexDirection: 'row',
        alignItems: 'center',
        paddingHorizontal: 24,
        paddingVertical: 16,
        borderBottomWidth: 1,
        borderBottomColor: colors.border,
        backgroundColor: selectedCountryCode === item.code ? colors.secondary : 'transparent',
      }}
    >
      <CountryFlag
        countryCode={item.code}
        size="lg"
        style={{ marginRight: 12 }}
      />
      <View style={{ flex: 1 }}>
        <Text style={{ 
          fontSize: 16, 
          fontWeight: selectedCountryCode === item.code ? '600' : '400',
          color: selectedCountryCode === item.code ? colors.foreground : colors.foreground 
        }}>
          {item.name}
        </Text>
        <Text style={{ fontSize: 12, color: colors.mutedForeground, marginTop: 2 }}>
          {item.code}
        </Text>
      </View>
      {selectedCountryCode === item.code && (
        <Ionicons 
          name="checkmark" 
          size={20} 
          color={colors.primary}
        />
      )}
    </TouchableOpacity>
  );

  if (initialLoading) {
    return (
      <SafeAreaView style={{ flex: 1, backgroundColor: colors.background }}>
        <ScreenHeader
          title={t('editFlag.title')}
          onBack={handleCancel}
        />
        <View className="flex-1 justify-center items-center px-6">
          <ActivityIndicator size="large" color={colors.primary} />
          <Text className={`text-base mt-4 text-center ${isDark ? 'text-gray-400' : 'text-gray-600'}`}>
            {t('editFlag.loading')}
          </Text>
        </View>
      </SafeAreaView>
    );
  }

  return (
    <SafeAreaView style={{ flex: 1, backgroundColor: colors.background }}>
      <ScreenHeader
        title={t('editFlag.title')}
        onBack={handleCancel}
      />

      {/* Search Bar */}
      <View className="px-6 pb-4">
        <View className={`flex-row items-center rounded-xl border px-4 py-3 ${
          isDark ? 'bg-gray-800 border-gray-700' : 'bg-white border-gray-300'
        }`}>
          <Ionicons
            name="search"
            size={20}
            color={colors.mutedForeground}
            style={{ marginRight: 12 }}
          />
          <TextInput
            className={`flex-1 text-base ${isDark ? 'text-white' : 'text-gray-900'}`}
            value={searchQuery}
            onChangeText={setSearchQuery}
            placeholder={t('editFlag.searchPlaceholder')}
            placeholderTextColor={colors.mutedForeground}
            returnKeyType="search"
          />
          {searchQuery.length > 0 && (
            <TouchableOpacity onPress={() => setSearchQuery('')}>
              <Ionicons 
                name="close-circle" 
                size={20} 
                color={colors.mutedForeground}
              />
            </TouchableOpacity>
          )}
        </View>
      </View>

      {/* Country List */}
      <FlatList
        data={filteredCountries}
        renderItem={renderCountryItem}
        keyExtractor={(item) => item.code}
        showsVerticalScrollIndicator={false}
        style={{ flex: 1 }}
        contentContainerStyle={{ paddingBottom: 100 }}
      />

      <BottomButtons
        onCancel={handleCancel}
        onSave={handleSave}
        loading={loading}
        disabled={!hasChanges}
        saveText={t('edit.save')}
      />
    </SafeAreaView>
  );
} 